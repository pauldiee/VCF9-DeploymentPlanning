#!/usr/bin/env node
/**
 * Drift guard: docs/06-deployment-plan.md vs the export tool's generator (#215).
 *
 * The doc is hand-written prose; the tool generates from src/lib/deployment-plan.ts.
 * Nothing made them agree, so they drifted twice without anyone noticing -- and in
 * BOTH directions: the doc gained the correct Avi placement first (#174) while the
 * tool kept the wrong one until #212, then the tool gained the License Hub stories
 * (#213) while the doc kept none.
 *
 * This does not try to generate the doc. `06` is deliberately rich prose -- rationale,
 * TechDocs links, gotchas -- that a generator would flatten. It only asserts that every
 * story the generator can emit is *documented somewhere*, so drift is loud instead of
 * silent. The prose stays hand-written; only its completeness is enforced.
 *
 * E9 is exempt by design: its Supervisor / Avi / licensing stories are numbered by
 * selection (`9.${stories.length + 1}`), so their numbers shift with the variant. The
 * doc describes them in prose instead, and says so.
 *
 * Run: node scripts/check-plan-sync.mjs   (exit 1 on drift)
 */
import { readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { build } from 'esbuild';

const here = dirname(fileURLToPath(import.meta.url));
const DOC = resolve(here, '../../docs/06-deployment-plan.md');
const LIB = resolve(here, '../src/lib/deployment-plan.ts');

/** Epics whose story numbering is selection-dependent -- documented in prose. */
const PROSE_NUMBERED_EPICS = new Set(['9']);

const { outputFiles } = await build({
  entryPoints: [LIB],
  bundle: true,
  format: 'esm',
  write: false,
  logLevel: 'error',
});
const mod = await import(
  'data:text/javascript;base64,' + Buffer.from(outputFiles[0].text).toString('base64')
);

/** Selections chosen to exercise every optional story the generator can emit. */
function selections() {
  const out = [];
  const base = () => mod.defaultSelection();

  out.push(['default', base()]);

  const everything = base();
  everything.mgmtStretched = true;
  everything.day2 = true;
  everything.vdefend = true;
  everything.automation = { deploy: true, model: 'medium', placement: 'overlay', aviLb: true };
  everything.day2Components = { logs: true, networks: true, identityBroker: true };
  out.push(['everything on', everything]);

  const deferred = base();
  deferred.automation.deploy = false;
  deferred.day2Components = { logs: false, networks: false, identityBroker: false };
  out.push(['automation deferred, no optional components', deferred]);

  const licensingOnly = base();
  licensingOnly.day2 = false;
  licensingOnly.vdefend = true;
  out.push(['licensing only (Day-2 off)', licensingOnly]);

  return out;
}

const doc = readFileSync(DOC, 'utf8');
const documented = new Set(
  [...doc.matchAll(/\*\*Story\s+(\d+\.\d+[a-z]?)\s/g)].map((m) => m[1]),
);

const missing = new Map(); // storyId -> {title, seenIn}
for (const [label, sel] of selections()) {
  for (const epic of mod.selectedEpics(sel)) {
    const epicNum = epic.id.replace(/^E/, '').split('-')[0];
    if (PROSE_NUMBERED_EPICS.has(epicNum)) continue;
    for (const story of epic.stories) {
      if (documented.has(story.id)) continue;
      if (!missing.has(story.id)) missing.set(story.id, { title: story.title, seenIn: label });
    }
  }
}

if (missing.size === 0) {
  console.log(`OK  every generated story is documented in 06-deployment-plan.md (${documented.size} story IDs found)`);
  process.exit(0);
}

console.error('DRIFT  the export tool emits stories that 06-deployment-plan.md does not document:\n');
for (const [id, { title, seenIn }] of [...missing].sort((a, b) => a[0].localeCompare(b[0], undefined, { numeric: true }))) {
  console.error(`  Story ${id.padEnd(6)} ${title}`);
  console.error(`  ${' '.repeat(12)}(generated by the "${seenIn}" selection)\n`);
}
console.error('Add them to docs/06-deployment-plan.md, or exempt the epic deliberately in this script.');
process.exit(1);
